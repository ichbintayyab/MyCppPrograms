#include <iostream>
using namespace std;

int main(){
	int x = 10, y = 20;
	
	// Relational operations
	cout<<"x == y: "<<(x == y)<<endl;
	cout<<"x != y: "<<(x != y)<<endl;
	cout<<"x > y: "<<(x > y)<<endl;
	cout<<"x < y: "<<(x < y)<<endl;
	cout<<"x >= y: "<<(x >= y)<<endl;
	cout<<"x <= y: "<<(x <= y)<<endl;
	
	return 0;
}
